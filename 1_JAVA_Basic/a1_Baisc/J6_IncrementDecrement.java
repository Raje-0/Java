package a1_Baisc;

public class J6_IncrementDecrement {

	public static void main(String[] args) {

		// post increment
		int i = 10;
		int b = i++;
		System.out.println(i);
		System.out.println(b);

		// pre increment
		int j = 2;
		int c = ++j;
		System.out.println(j);
		System.out.println(c);
	}

}
