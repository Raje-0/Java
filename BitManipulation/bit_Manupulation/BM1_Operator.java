package bit_Manupulation;

public class BM1_Operator {

	public static void main(String[] args) {
		//AND OPERATOR
		System.out.println(5 & 6);
		
		//OR OPRATOR
		System.out.println(5 |6);
		
		//XOR OPERATOR
		System.out.println(5^6);
		
		//~ OPERATOR
		System.out.println(~5);
		
		//XOR OPERATOR
		System.out.println(~0);
	}

}
