package a1_Baisc;

public class J4_Operator {

	public static void main(String[] args) {

		int a, b, c, d, e, f;

//Arithmetic

		a = 6;
		b = 2;
		c = a + b;
		d = a - b;

		e = a * b;

		f = a / b;

		System.out.println("add = " + c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);

		int m, n;
		m = 6;
		n = 4;
		double o = (double) m / n;

		int p = m % n; // reminder(modular operator)

		System.out.println(o);
		System.out.println(p);

		// Unary
		

	}

}
